#include <stdio.h> // Biblioteca de comunicação com o usuário
#include <stdlib.h> // Biblioeteca de alocação de espaço em memória
#include <locale.h> // Bibioteca de alocações de texto por região
#include <string.h> // Biblioteca responsável pelas string

int registro() // Função para cadastrar usuários
{
	// inicio criação variaveis
	setlocale(LC_ALL, "Portuguese");
	
	char arquivo[40];
	char cpf[40]; // Variável para registrar CPF
	char nome[40]; // Variável para registrar nome
	char sobrenome[40]; // Variável para registrar sobrenome
	char cargo[40]; // Variável para registrar cargo
	// fim criação variaveis
	
	printf("Digite o CPF a ser cadastrado: ");
	scanf("%s",cpf); // Armazena no string
	
	strcpy(arquivo, cpf); // Atribui string do CPF para o arquivo
	
	FILE *file; // Cria o arquivo no banco de dados
	file = fopen(arquivo, "w"); // Cria o arquivo no banco de dados (w=novo)
	fprintf(file,cpf); // Salvo o valor da variável no bando de dados
	fclose(file); // Fecha o arquivo
	
	file = fopen(arquivo, "a"); // (a=atualização)
	fprintf(file, ",");
	fclose(file);
	
	printf("Digite o nome a ser cadastrado: ");
	scanf("%s",nome); 
	
	file = fopen(arquivo, "a");
	fprintf(file,nome); 
	fclose(file); 
	
	file = fopen(arquivo, "a");
	fprintf(file, ",");
	fclose(file);
	
	printf("Digite o sobrenome a ser cadastrado: ");
	scanf("%s",sobrenome); 
	
	file = fopen(arquivo, "a");
	fprintf(file,sobrenome); 
	fclose(file);
	
	file = fopen(arquivo, "a");
	fprintf(file, ",");
	fclose(file);
	
	printf("Digite o cargo a ser cadastrado: ");
	scanf("%s",cargo);
	
	file = fopen(arquivo, "a");
	fprintf(file,cargo); 
	fclose(file);
	
	system("pause");
	
}

int consultar()
{
	setlocale(LC_ALL, "Portuguese");
	
	char cpf[40];
	char conteudo[200];
	
	printf("Digite o CPF a ser consultado: ");
	scanf("%s",cpf);
	
	FILE *file;
	file = fopen(cpf,"r");
	
	if(file == NULL)
	{
		printf("Não foi possível abrir o arquivo, não localizado!\n");
	}
	
	while(fgets(conteudo, 200, file) != NULL)
	{
		printf("\nEssas são as informações do usuário: ");
		printf("%s", conteudo);
		printf("\n\n");
	}
	
	system("pause");
}

int deletar()
{
	char cpf[40];
	
	printf("Digite o CPF do usuário que deseja deletar:");
	scanf("%s",cpf);
	
	remove(cpf);
	
	FILE *file;
	file = fopen(cpf,"r");
	
	if(file == NULL)
	{
		printf("O usuário não se encontra no sistema!. \n");
		system("pause");
	}
}

